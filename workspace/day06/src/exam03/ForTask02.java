package exam03;

public class ForTask02 {
	public static void main(String[] args) {

//		<실습>
//		for문
//		1번째 송창석
//		...
//		10번째 송창석
		for(int i = 1; i<11; i++) {
			System.out.println(i + "번째 송창석");
		}
		System.out.println();
		for(int i = 0; i<10; i++) {
			System.out.println(i+1 + "번째 송창석");
		}
	}
}
